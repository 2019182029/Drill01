import turtle

def turtle_up() :
    turtle.setheading(90)
    turtle.forward(50)
    
def turtle_left() :
    turtle.setheading(1800)
    turtle.forward(50)
    
def turtle_down() :
    turtle.setheading(-90)
    turtle.forward(50)
    
def turtle_right() :
    turtle.setheading(0)
    turtle.forward(50)

